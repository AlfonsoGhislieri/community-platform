export const FlagIcon = (props: FlagIconProp) => {
  const size = props.width || 20
  const height = size * 0.75
  return countryCodes.includes(props.code.toUpperCase()) ? (
    <Box
      {...(props as any)}
      sx={{
        display: 'inline-block',
        overflow: 'hidden',
        lineHeight: 0,
        width: `${size}px !important`,
        height: `${height}px`,
        borderRadius: '3px',
      }}
    >
      <ReactCountryFlag
        svg
        countryCode={props.code}
        cdnSuffix="svg"
        style={{
          width: `${size}px`,
          height: height + 'px',
        }}
      >
        {props.children}
      </ReactCountryFlag>
    </Box>
  ) : (
    <>
      <Box
        style={{
          borderRadius: '3px',
          background: '#282727',
          width: `${size}px`,
          height: height + 'px',
          display: 'flex',
          justifyContent: 'center',
          alignItems: 'center',
        }}
      >
        <img
          src="https://community.preciousplastic.com/static/media/icon-star-active.e35a3de2.svg"
          alt=""
          width={`${size * 0.75}px`}
          height={height * 0.75 + 'px'}
        />
      </Box>
    </>
  )
}
